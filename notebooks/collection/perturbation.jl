# ‚ïî‚ïê‚ï° dcada800-7683-4748-b46f-f79955120c0b
md"""
En ciencia, la gran mayor√≠a de problemas no tienen soluci√≥n anal√≠tica. Sin embargo, es frecuente el caso de que un problema complicado se puede escribir en t√©rminos de un problema conocido m√°s una desviaci√≥n peque√±a respecto del original. La teor√≠a de perturbaciones se encarga de darnos las herramientas para obtener una aproximaciones anal√≠ticas a la soluci√≥n real en funci√≥n de un par√°metro peque√±o.

Por ejemplo, imaginemos que queremos obtener la ra√≠z real de la ecuaci√≥n qu√≠ntica

$$x^5 + \epsilon x = 1,$$

donde $\epsilon$ es un par√°metro peque√±o. En el caso en el que eliminamos esta perturbaci√≥n, $\epsilon = 0$, la soluci√≥n real es

$$x^{(0)} = 1.$$

Para aproximar la soluci√≥n del problema completo, escribimos un desarrollo en potencias de $\epsilon$

$$x = x^{(0)} + œµa_1 + œµ^2 a_2 + \cdots = 1 + œµa_1 + œµ^2 a_2 + \cdots .$$

Si sustitimos este desarrollo en el problema original, y agrupamos seg√∫n potencias de $\epsilon$, se obtiene

$$\epsilon(1 + 5a_1) + \epsilon^2(5a_2 + 10a_1^2 + a_1) + O(\epsilon^3) = 0.$$

Separando por los diferentes √≥rdenes de $\epsilon$, se llega a la jerarqu√≠a de ecuaciones

$$O(\epsilon): \ 5a_1 = -1,$$
$$O(\epsilon^2): \ 5a_2 = -a_1 - 10a_1^2$$
$$\vdots$$
donde para resolver un orden necesitamos el anterior. As√≠, se obtiene que $a_1 = -0.2$ y $a_2 = - 0.04$, por lo que la aproximaci√≥n a la ra√≠z real para $\epsilon$ peque√±o es

$$x = 1 - 0.2 \epsilon - 0.04 \epsilon^2 + O(\epsilon^3)$$
"""

# ‚ïî‚ïê‚ï° 91025722-35e1-4534-b82c-099aa7857eef
md"""
En este cuaderno, queremos generalizar este proceso para cualquier orden y dejar que la librer√≠a `Symbolics.jl` de Julia resuelva este problema con variables simb√≥licas.

En primer lugar, podemos elegir el n√∫mero de t√©rminos en $\epsilon$ que queremos que nuestro desarrollo tenga
"""

# ‚ïî‚ïê‚ï° f72d661f-7b54-4bd9-921d-ea2de8748c63
md"""
``n = `` $(@bind n Slider(1:10; show_value = true, default = 2))
