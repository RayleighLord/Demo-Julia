# ‚ïî‚ïê‚ï° 1c029a9e-6fcb-481e-83bb-83f0a20f7907
md"""
A continuaci√≥n, creamos las variables simb√≥licas $\epsilon$ y $a$, donde $a$ es una matriz que contiene los coeficientes $a_1, a_2, ...$.
"""

# ‚ïî‚ïê‚ï° cf28cb2e-6bc4-4847-82db-418d94302bde
@variables a[1:n] œµ

# ‚ïî‚ïê‚ï° 695b9bad-181f-4d89-99e3-9ab52ab574ff
md"""
Generamos una funci√≥n que calcule el desarrollo en potencias de $\epsilon$
"""

# ‚ïî‚ïê‚ï° d6b9b148-cb43-46b1-9d47-4b85e1fdccc0
def_taylor(x, ps) = sum([a * x^i for (i, a) in enumerate(ps)])

# ‚ïî‚ïê‚ï° 0ad610de-01cf-4e7e-adcb-b27168d22b3f
md"""
Escribimos la aproximaci√≥n a la ra√≠z real $x$ como el desarrollo en serie de potencias de $\epsilon$ anteriormente mencionado
"""

# ‚ïî‚ïê‚ï° 17947515-a73f-4528-a445-f9646975f4e9
x = 1 + def_taylor(œµ, a)

# ‚ïî‚ïê‚ï° a4af482d-7133-46e3-b89a-8ec8c875282a
md"""
y definimos la ecuaci√≥n que queremos resolver en funci√≥n de las variables simb√≥licas
"""

# ‚ïî‚ïê‚ï° 0b95daa1-4b4f-4a03-8725-a157ed29619f
eq = x^5 + œµ * x - 1

# ‚ïî‚ïê‚ï° 6044a682-1ae5-41d9-9949-ba2adc922660
expand(eq)

# ‚ïî‚ïê‚ï° ae9a00c3-b7d5-40a7-b9ec-0396b5b24c0e
md"""
Ahora, vamos a generar una funci√≥n que agrupe los t√©rminos orden a orden de $\epsilon$ para luego irlos resolviendo secuencialmente. Esto se hace en la funci√≥n `collect_powers`
"""

# ‚ïî‚ïê‚ï° 1db92443-522e-40e0-9b63-64997805ba2a
function collect_powers(eq, x, ns; max_power=100)
    eq = substitute(expand(eq), Dict(x^j => 0 for j in last(ns)+1:max_power))

    eqs = []
    for i in ns
        powers = Dict(x^j => (i == j ? 1 : 0) for j in 1:last(ns))
        push!(eqs, substitute(eq, powers))
    end
    eqs
end

# ‚ïî‚ïê‚ï° db0eb9e2-40de-44ea-ab31-4e8c9ce2c4bd
md"""
Como se puede ver en la siguiente celda, se obtienen exactamente las mismas ecuaciones para los dos primeros √≥rdenes en $\epsilon$
"""

# ‚ïî‚ïê‚ï° 5e74e034-9f59-4bf0-b9bc-e3e0dfbf2294
eqs = collect_powers(eq, œµ, 1:n)

# ‚ïî‚ïê‚ï° 2f9cf10d-5c74-4265-8b98-b2bdbb5093f7
md"""
Ahora solo falta una funci√≥n que resuelva todas las ecuaciones anteriores para los valores de los coeficientes $a_n$
"""

# ‚ïî‚ïê‚ï° 168cb7ff-2dd1-4620-9220-f505863c59bc
function solve_coef(eqs, ps)
    vals = Dict()

    for i in eachindex(ps)
        eq = substitute(eqs[i], vals)
        vals[ps[i]] = Symbolics.solve_for(eq ~ 0, ps[i])
    end
    vals
end

# ‚ïî‚ïê‚ï° 2ce98fbd-b317-4ac3-8635-b0a0e560f79e
md"""
Los resultados de los coeficientes se almacenan en el siguiente diccionario
"""

# ‚ïî‚ïê‚ï° bf8cb5cd-e447-4b43-8b8b-5faaebdd2b79
vals = solve_coef(eqs, a)

# ‚ïî‚ïê‚ï° 9a6ed16b-579f-4f28-9780-6b5aae6ea3f5
md"""
